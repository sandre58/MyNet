namespace MyNet.UI;

/// <summary>
/// Specifies the action to perform on visibility, such as show, hide, or toggle.
/// </summary>
public enum VisibilityAction
{
    /// <summary>
    /// Show the element.
    /// </summary>
    Show,

    /// <summary>
    /// Hide the element.
    /// </summary>
    Hide,

    /// <summary>
    /// Toggle the visibility of the element.
    /// </summary>
    Toggle
}
